package project.management.system;

public class Department
{
    private int Id;
    private String Name;

    
    public int getId()
    {
        return Id;
    }

    public void setId(int ID)
    {
        this.Id = ID;
    }

    public String getName()
    {
        return Name;
    }

    public void setName(String Name)
    {
        this.Name = Name;
    }
    
}
