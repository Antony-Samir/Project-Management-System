package project.management.system;

public class Resource
{
    private int Id;
    private String Name;
    private int Used;

    public int getId()
    {
        return Id;
    }

    public void setId(int Id)
    {
        this.Id = Id;
    }

    public String getName()
    {
        return Name;
    }

    public void setName(String Name)
    {
        this.Name = Name;
    }

    public int getUsed()
    {
        return Used;
    }

    public void setUsed(int Used)
    {
        this.Used = Used;
    }
    
    
}
