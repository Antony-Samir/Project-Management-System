package project.management.system;

public class Resource
{
    private int ID;
    private String Name;
    private int Used;

    
    
    public int getID()
    {
        return ID;
    }

    public void setID(int ID)
    {
        this.ID = ID;
    }

    public String getName()
    {
        return Name;
    }

    public void setName(String Name)
    {
        this.Name = Name;
    }

    public int getUsed()
    {
        return Used;
    }

    public void setUsed(int Used)
    {
        this.Used = Used;
    }
    
    
}
